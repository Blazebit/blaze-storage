package com.blazebit.storage.rest.model.config;

import java.io.Serializable;

public class StorageTypeConfigEntryRepresentation implements Serializable {

	private static final long serialVersionUID = 1L;

	private String key;
	private String value;

	public StorageTypeConfigEntryRepresentation() {
	}

	public StorageTypeConfigEntryRepresentation(String key, String value) {
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		StorageTypeConfigEntryRepresentation other = (StorageTypeConfigEntryRepresentation) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}
	
}
